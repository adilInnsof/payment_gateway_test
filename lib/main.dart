class MyHomePage extends StatefulWidget {
  const MyHomePage({super.key});

  @override
  State<MyHomePage> createState() => _MyHomePageState();
}

class _MyHomePageState extends State<MyHomePage> {
  String errorCode = "";
  String message = "";
  checkPayment() async {
    MFInitiatePaymentRequest request = MFInitiatePaymentRequest(
        invoiceAmount: 10, currencyIso: MFCurrencyISO.SAUDIARABIA_SAR);
    await MFSDK.initiatePayment(request, MFLanguage.ENGLISH).then((value) {
      print(value.paymentMethods.toString());
    }).catchError((error) {
      print(error.message);
      if (error is MFError) {
        setState(() {
          errorCode = error.code ?? "";
          message = error.message ?? "";
        });
      }
    });
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: SizedBox(
        width: double.infinity,
        child: Column(
            crossAxisAlignment: CrossAxisAlignment.center,
            mainAxisAlignment: MainAxisAlignment.center,
            children: [
              ElevatedButton(
                  onPressed: checkPayment, child: const Text('checkPayment')),
              const SizedBox(
                height: 10,
              ),
              Text("errcode: $errorCode"),
              const SizedBox(
                height: 10,
              ),
              Text("message: $message"),
            ]),
      ),
    );
  }
}
